    public void filing() throws IOException {
        String file1 = "MyCV.txt";

        FileWriter file = new FileWriter(file1,true);

        SkillsInformation obj = new SkillsInformation();
        obj.osSkills = returnOS();
        obj.appSkills = returnApp();
        obj.programmingSkills = retunProgrammingLanguages();
        obj.editingSkills = returnEditing();
        obj.others = returnOtherAchievements();
        obj.awards = returnAchievements();
        obj.languages = returnLanguages();

        file.write(obj.writeSkills() + obj.writeLanguages() + obj.writeawards());
        file.close();
    }

