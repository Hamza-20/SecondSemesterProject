    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (Languages.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter languages you are familiar with");
        } else if (OS.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Operating Systems you are familiar with");
        } else if (App.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter applications you are familiar with");
        } else if (Editing.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter editing softwares you are familiar with");
        } else if (Programming.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter programming languages you are familiar with");
        } else if (Awards.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please mention your achievements");
        } else if (Other.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please mention if you have any others achievements otherwise enter no");
        } else {
            try {
                filing();
            } catch (IOException ex) {
                Logger.getLogger(Resume_Builder_skill.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

